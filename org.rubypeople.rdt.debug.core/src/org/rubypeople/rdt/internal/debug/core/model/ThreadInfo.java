package org.rubypeople.rdt.internal.debug.core.model;

public class ThreadInfo {
	private int id ;
	private String status ;
	public int getId() {
		return id;
	}

	public String getStatus() {
		return status;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
