      <td valign="top"><font face="arial,helvetica,geneva" size="-1"><b>Trac site, developer blog, and Google Summer of Code (2006-07-26)</b>
        <br/>
<p>Sorry, we've been pretty lax in updating our Sourceforge site and as a result a number of people have gotten the impression the project is dead.</p>

<p>We're not dead!</p>

<p>We've migrated to using a Trac site for both our Wiki and bug tracking. You can find it at <a href="http://rubyeclipse.mktec.com/cgi-bin/trac.py/wiki">http://rubyeclipse.mktec.com/cgi-bin/trac.py/wiki</a></p>

<p>Also, you can always subscribe to my blog, <a href="http://cwilliams.textdriven.com">Late to the Party</a>, for personal updates on RDT related work (among other topics like Ruby and Rails).</p>

<p>Lastly, I'd like to make a very belated announcement here that we have two projects going on with students working on RDT. The first is ongoing 
<a href="http://morki.ch/rubyrefactoring">work by Swiss students</a> on bringing code generation and refactoring to RDT.
They've already finished phase one and have a modified version of RDT available for download. We're working with them to merge their changes into RDT's trunk (and <a href="http://jruby.codehaus.org/">JRuby</a>).</p>

<p>Also we have a project accepted for Google's Summer of Code. <a href="http://soc.jayunit.net/">Jason Morrison</a> is doing work on type inferrence for Ruby and we're integrating that with RDT. 
We've already had some success in doing some initial groundwork such as <a href="http://soc.jayunit.net/articles/2006/07/21/the-one-where-more-occurrences-get-marked-and-subsequently-hit-the-big-screen">Marking occurrences of variables</a>.</p>
